public class Operadores {

    public static void main(String[] args) {
        int a, b, c, d;

        a = 5;
        b = 6;
        String resultado = a == b ? "verdadeiro" : "falso";

        System.out.println(resultado);

        c = 8;
        d = 8;
        String resultado2 = c == d ? "verdadeiro" : "falso";

        System.out.println(resultado2);

    }
}
